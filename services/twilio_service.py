import os
from twilio.rest import Client
from typing import Optional

class TwilioService:
    """Service for sending SMS notifications via Twilio"""
    
    def __init__(self):
        self.account_sid = os.getenv("TWILIO_ACCOUNT_SID")
        self.auth_token = os.getenv("TWILIO_AUTH_TOKEN")
        self.phone_number = os.getenv("TWILIO_PHONE_NUMBER")
        
        if not all([self.account_sid, self.auth_token, self.phone_number]):
            print("âš ï¸ Twilio credentials not complete. SMS functionality will be limited.")
            self.client = None
        else:
            try:
                self.client = Client(self.account_sid, self.auth_token)
                print("âœ… Twilio service initialized")
            except Exception as e:
                print(f"âŒ Twilio initialization failed: {e}")
                self.client = None
    
    def send_sms(self, to_phone_number: str, message: str) -> bool:
        """Send SMS message to phone number"""
        if not self.client:
            print("âŒ Twilio client not available")
            return False
        
        try:
            # Ensure phone number is in international format
            if not to_phone_number.startswith('+'):
                # Assume Indian number if no country code
                if to_phone_number.startswith('91'):
                    to_phone_number = '+' + to_phone_number
                elif len(to_phone_number) == 10:
                    to_phone_number = '+91' + to_phone_number
                else:
                    to_phone_number = '+91' + to_phone_number
            
            # Send SMS
            sms_message = self.client.messages.create(
                body=message,
                from_=self.phone_number,
                to=to_phone_number
            )
            
            print(f"âœ… SMS sent successfully. SID: {sms_message.sid}")
            return True
            
        except Exception as e:
            print(f"âŒ Failed to send SMS: {e}")
            return False
    
